def analyze_dimensions(dimensions: PRISMDimensions) -> dict[str, Any]:
    """
    Analyze PRISM dimensions to identify primary/secondary preferences,
    behavioral patterns, and insights.
    """
    scores = {
        "gold": dimensions.gold,
        "green": dimensions.green,
        "blue": dimensions.blue,
        "red": dimensions.red,
    }
    sorted_dims = sorted(scores.items(), key=lambda x: x[1], reverse=True)
    primary = sorted_dims[0][0]
    secondary = sorted_dims[1][0]

    # Generate insights based on dimension balance
    insights = []
    spread = sorted_dims[0][1] - sorted_dims[-1][1]

    if spread <= 15:
        insights.append(
            "You have a well-balanced profile across all four dimensions, "
            "giving you natural versatility in different situations."
        )
    elif spread >= 40:
        insights.append(
            f"You have a strong preference for {_dim_label(primary)} behaviors. "
            f"This is a real strength — and you can also develop flexibility "
            f"in your {_dim_label(sorted_dims[-1][0])} dimension when needed."
        )

    # Primary-secondary interaction insights
    combo_insights = _get_combo_insight(primary, secondary)
    if combo_insights:
        insights.append(combo_insights)

    # Communication preferences
    communication = _get_communication_preferences(primary, secondary)

    # Growth areas (lowest dimensions)
    growth_areas = [
        f"Developing your {_dim_label(dim)} side could help you "
        f"{_growth_suggestion(dim)}"
        for dim, score in sorted_dims[2:]
        if score < 50
    ]

    # Stress behaviors
    stress_behaviors = _get_stress_behaviors(primary)

    return {
        "primary": primary,
        "secondary": secondary,
        "insights": insights,
        "communication": communication,
        "growth_areas": growth_areas,
        "stress_behaviors": stress_behaviors,
    }


def _dim_label(dim: str) -> str:
    """Human-readable label for a PRISM dimension."""
    return {
        "gold": "Gold (structured, detail-oriented)",
        "green": "Green (empathetic, people-focused)",
        "blue": "Blue (analytical, data-driven)",
        "red": "Red (action-oriented, results-driven)",
    }.get(dim, dim)


def _get_combo_insight(primary: str, secondary: str) -> Optional[str]:
    """Insight based on primary-secondary combination."""
    combos = {
        ("gold", "blue"): (
            "Your Gold-Blue combination means you bring both structure and "
            "analytical depth. You're likely excellent at creating systems "
            "that are both organized and data-informed."
        ),
        ("gold", "green"): (
            "Your Gold-Green combination blends structure with empathy. "
            "You likely excel at creating organized environments where "
            "people feel supported and clear about expectations."
        ),
        ("green", "red"): (
            "Your Green-Red combination brings people-awareness and action "
            "together. You likely motivate others while keeping relationships "
            "strong."
        ),
        ("blue", "red"): (
            "Your Blue-Red combination merges analytical thinking with "
            "decisive action. You likely excel at making data-driven "
            "decisions quickly."
        ),
        ("red", "gold"): (
            "Your Red-Gold combination blends drive with discipline. "
            "You likely get results through structured, efficient approaches."
        ),
        ("green", "blue"): (
            "Your Green-Blue combination brings empathy and analysis together. "
            "You likely understand people deeply and can back up your "
            "intuitions with data."
        ),
    }
    key = (primary, secondary)
    reverse_key = (secondary, primary)
    return combos.get(key) or combos.get(reverse_key)


