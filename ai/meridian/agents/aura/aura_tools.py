def _growth_suggestion(dim: str) -> str:
    """Growth suggestion for a dimension."""
    return {
        "gold": "bring more structure and attention to detail in complex projects",
        "green": "strengthen relationships and read interpersonal dynamics more naturally",
        "blue": "make more data-informed decisions and strengthen analytical reasoning",
        "red": "take more decisive action and increase your pace when needed",
    }.get(dim, "expand your behavioral range")


