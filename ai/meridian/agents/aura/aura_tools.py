from __future__ import annotations

from typing import Any, Optional
from pydantic import BaseModel, Field
from prism_inspire.core.log_config import logger


class PRISMDimensions(BaseModel):
    """PRISM Brain Mapping dimension scores (0-100)."""
    gold: int = Field(ge=0, le=100, description="Structured, detail-oriented")
    green: int = Field(ge=0, le=100, description="Empathetic, people-focused")
    blue: int = Field(ge=0, le=100, description="Analytical, data-driven")
    red: int = Field(ge=0, le=100, description="Action-oriented, results-driven")


class BehavioralPreferenceMap(BaseModel):
    """Complete Behavioral Preference Map generated by Aura."""
    user_id: str
    dimensions: PRISMDimensions
    primary_preference: str  # highest dimension
    secondary_preference: str  # second highest
    behavioral_insights: list[str] = Field(default_factory=list)
    communication_preferences: dict[str, Any] = Field(default_factory=dict)
    growth_areas: list[str] = Field(default_factory=list)
    stress_behaviors: list[str] = Field(default_factory=list)
    version: int = 1
    assessed_at: Optional[str] = None

    def to_context_dict(self) -> dict[str, Any]:
        """Convert to a dict suitable for injection as behavioral_context."""
        return {
            "user_id": self.user_id,
            "dimensions": self.dimensions.model_dump(),
            "primary_preference": self.primary_preference,
            "secondary_preference": self.secondary_preference,
            "insights": self.behavioral_insights,
            "communication": self.communication_preferences,
            "growth_areas": self.growth_areas,
        }


